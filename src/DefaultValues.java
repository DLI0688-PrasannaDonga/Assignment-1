public class DefaultValues {
    static byte byteNum;
    static short shortNum;
    static int intNum;
    static long longNum;
    static float floatNum;
    static double doubleNum;
    static char charVal;
    static boolean booleanVal;


    public static void main(String[] args) {

        System.out.println("Default values of all primitive data types");
        System.out.println("Byte :" + byteNum);
        System.out.println("Short :" + shortNum);
        System.out.println("Int :" + intNum);
        System.out.println("Long :" + longNum);
        System.out.println("Float :" + floatNum);
        System.out.println("Double :" + doubleNum);
        System.out.println("Char :" + charVal);
        System.out.println("Boolean :" + booleanVal);

    }
}
